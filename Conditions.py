# tim = joe (False)
# 18 > 2 (True)

# 5 != 5 - (False)
# 'Tim' != 'Hello' - (True)

# 1 < 2 - (True)

print(2 < 3) # True
print(2 > 3) # False

print("Hello" == "Helo") # False
print("Hello" != "Helo") # True

print("Hello" == "hello") # False, capitals matter.

print(2 < 3) # True
print(2 - 3 + 4 > 5) # False
print(False == True) # False

